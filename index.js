function meetAutoReactions() { // —Ñ—É–Ω–∫—Ü—ñ—è –¥–ª—è –ø–æ—à—É–∫—É —Ç–∞ –ø–æ–≤–µ—Ä–Ω–µ–Ω–Ω—è –ø–µ—Ä—à–æ–≥–æ –µ–ª–µ–º–µ–Ω—Ç–∞ —É –≤–µ–±-—Å—Ç–æ—Ä—ñ–Ω—Ü—ñ, —â–æ –≤—ñ–¥–ø–æ–≤—ñ–¥–∞—î –∑–∞–¥–∞–Ω–æ–º—É XPath-–≤–∏—Ä–∞–∑—É
  function getElement(xpath) {
    return document.evaluate( // v–µ—Ç–æ–¥ `document.evaluate` - –ø–æ—à—É–∫ –µ–ª–µ–º–µ–Ω—Ç–∞ –∑–∞ –∑–∞–¥–∞–Ω–∏–º —à–ª—è—Ö–æ–º XPath —ñ –ø–æ–≤–µ—Ä—Ç–∞—î –∑–Ω–∞–π–¥–µ–Ω–∏–π –µ–ª–µ–º–µ–Ω—Ç.
      xpath,
      document,
      null,
      XPathResult.FIRST_ORDERED_NODE_TYPE,
      null,
    ).singleNodeValue;
  }
  function getEmojiButton(index) {
    const emojis = ['üíñ', 'üëç', 'üéâ', 'üëè', 'üòÇ', 'üòÆ', 'üò¢', 'ü§î', 'üëé']; // —Å–ø–∏—Å–æ–∫ —ñ–∫–æ–Ω–æ–∫ –µ–º–æ–¥–∂—ñ 
    return getElement(`//button/img[@data-emoji="${emojis[index]}"]`); // –ø–æ—à—É–∫ –∫–Ω–æ–ø–∫–∏ —è–∫–∞ –º–∞—î –∑–æ–±—Ä–∞–∂–µ–Ω–Ω—è –∑–º—ñ–Ω–Ω–æ—ó –ø–æ –∞—Ç—Ä–∏–±—É—Ç—É data-emoji –∑ XPath-–≤–∏—Ä–∞–∑—É
  }

  const sendReactionButton = getElement('//i[text()="mood"]'); // –∫–Ω–æ–ø–∫–∞ —è–∫–∞ –≤–∏–∫–ª–∏–∫–∞—î –ø–µ—Ä–µ–ª—ñ–∫ –µ–º–æ–¥–∂—ñ 
  
  const intervalIds = []; // –ø—É—Å—Ç–∏–π –º–∞—Å–∏–≤ —ñ–Ω—Ç–µ—Ä–≤–∞–ª—É 

  return ({
    startReacting(index, timeout = 1000) {
      if (!getEmojiButton(index)) sendReactionButton.click(); // —è–∫—â–æ –∫–Ω–æ–ø–∫–∞ –∫–Ω–æ–ø–∫–∞ emoji –∑–∞ –ø–µ–≤–Ω–∏–º —ñ–Ω–¥–µ–∫—Å–æ–º (`index`) –Ω–∞ —Å—Ç–æ—Ä—ñ–Ω—Ü—ñ –Ω–µ –ø—Ä–∏—Å—É—Ç–Ω—è, —Ç–æ–¥—ñ –≤—ñ–¥–±—É–≤–∞—î—Ç—å—Å—è –∫–ª—ñ–∫ –ø–æ –Ω—ñ–π 
      intervalIds.push(setInterval(() => { // –∫–ª—ñ–∫–∞–Ω–Ω—è –Ω–∞ –∫–Ω–æ–ø–∫—É  emoji –∑ –ø–µ–≤–Ω–∏–º —ñ–Ω–¥–µ–∫—Å–æ–º —á–µ—Ä–µ–∑ timeout = 1000 —á–∞—Å—É.
        try {
          getEmojiButton(index).click(); // —è–∫—â–æ –∫–Ω–æ–ø–∫–∞ —î, —ñ —ñ–Ω–¥–µ–∫—Å –∑–Ω–∞—Ö–æ–¥–∏—Ç—å —Ç–æ –≤—ñ–¥–±—É–≤–∞—î—Ç—å—Å—è –∫–ª—ñ–∫ –Ω–∞ —Ü—é –µ–º–æ–¥–∂—ñ 
        } catch {
          this.stopReacting(); // —è–∫—â–æ –∫–Ω–æ–ø–∫–∏ –Ω–µ–º–∞—î, —á–∏ –ø—Ä–∏ –æ—Ç—Ä–∏–º–∞–Ω–Ω—ñ –¥–æ—Å—Ç—É–ø—É –¥–æ –∫–Ω–æ–ø–∫–∏ —î –ø–æ–º–∏–ª–∫–∞ - —Ç–æ–¥—ñ –≤—ñ–¥–±—É–≤–∞—î—Ç—å—Å—è –ø—Ä–∏–ø–∏–Ω–µ–Ω–Ω—è –Ω–∞—Ç–∏—Å–∫–∞–Ω–Ω—è –Ω–∞ —Ä–µ–∞–∫—Ü—ñ—é 
        }
      }, timeout));
    },
    stopReacting() {
      intervalIds.forEach(id => clearInterval(id)); // –∑–∫–ø–∏–Ω—è—î –∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω—É —Ä–µ–∞—Ü—ñ—é, –ø—ñ—Å–ª—è —Ç–æ–≥–æ —è–∫ –ø–µ—Ä–µ–±–µ—Ä–∏ –≤—Å—ñ id —ñ–Ω—Ç–µ—Ä–≤–∞–ª—ñ–≤ –≤ –º–∞—Å–∏–≤—ñ const intervalIds = []; –ø—ñ—Å–ª—è —á–æ–≥–æ –æ—á–∏—â—è—î –º–∞—Å–∏–≤ clearInterval(id)
      intervalIds.length = 0; // –ø–µ—Ä–µ–≤—ñ—Ä—è—î –¥–æ–≤–∂–∏–Ω—É —ñ–Ω—Ç–µ—Ä–≤–∞–ª—É —è–∫–∞ –º–∞—î –±—É—Ç–∏ 0 
    }
  });
}

